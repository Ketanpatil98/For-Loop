//Reverse Order Printing Print numbers from 100 to 1 in reverse order using a for loop.

package com.Forloop;

public class Reverseorder {
	
	public static void main(String[] args) {
		
		for (int i = 100 ; i >= 1 ; i--) {
			
			System.out.println("the number are from 100 to 1 " +i);
		}
	}

}
