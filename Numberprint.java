//Print Numbers from 1 to 100 Write a program that prints numbers from 1 to 100 using a for loop.


package com.Forloop;

public class Numberprint {
	
	public static void main(String[] args) {
		
		for (int i = 1; i<=100; i++) {
			System.out.println("The number from 1 to 100 are "  + i);
		}
	}

}
